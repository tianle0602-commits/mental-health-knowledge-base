/**
 * 根据子情绪名称获取数据
 */
export function getSubEmotion(name: string): SubEmotion | undefined {
  for (const core of emotionWheel) {
    const found = core.subEmotions.find(s => s.name === name);
    if (found) return found;
  }
  return undefined;
}

/**
 * 根据情绪标签匹配核心情绪
 */
export function findCoreEmotion(emotionKey: string): CoreEmotion | undefined {
  return emotionWheel.find(core =>
    core.subEmotions.some(sub => sub.emotionKey === emotionKey)
  );
}