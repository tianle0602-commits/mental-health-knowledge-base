// ==================== 情绪匹配逻辑 ====================

interface EmotionMatch {
  emotion: string;
  score: number;
  matchedKeywords: string[];
}

function findEmotionMatches(query: string): EmotionMatch[] {
  const matches: EmotionMatch[] = [];
  const queryLower = query.toLowerCase();

  for (const [emotionName, config] of Object.entries(emotionConfigs)) {
    const matched: string[] = [];
    let score = 0;

    for (const keyword of config.keywords) {
      if (queryLower.includes(keyword.toLowerCase())) {
        matched.push(keyword);
        // 长的关键词匹配得分更高（更精确）
        score += keyword.length >= 4 ? 3 : keyword.length >= 2 ? 2 : 1;
      }
    }

    if (matched.length > 0) {
      matches.push({ emotion: emotionName, score, matchedKeywords: matched });
    }
  }

  // 按得分降序
  matches.sort((a, b) => b.score - a.score);

  return matches;
}

function calculateIntensity(matches: EmotionMatch[], queryLength: number): number {
  if (matches.length === 0) return 5;

  // 基于匹配数量和关键词长度计算强度
  const topScore = matches[0].score;
  const totalKeywords = matches[0].matchedKeywords.length;

  // 强度受匹配得分和关键词密度影响
  let intensity = 3;

  if (topScore >= 10) intensity = 9;
  else if (topScore >= 7) intensity = 8;
  else if (topScore >= 5) intensity = 7;
  else if (topScore >= 3) intensity = 6;
  else if (topScore >= 2) intensity = 5;
  else intensity = 4;

  // 如果匹配了多个情绪，强度可能更高
  if (matches.length >= 3) intensity = Math.min(intensity + 1, 10);

  // 如果总关键词多，强度更高
  if (totalKeywords >= 5) intensity = Math.min(intensity + 1, 10);

  return intensity;
}

function collectCategories(matches: EmotionMatch[]): string[] {
  const categorySet = new Set<string>();
  for (const match of matches) {
    const config = emotionConfigs[match.emotion];
    if (config) {
      for (const cat of config.categories) {
        categorySet.add(cat);
      }
    }
  }
  return Array.from(categorySet);
}

