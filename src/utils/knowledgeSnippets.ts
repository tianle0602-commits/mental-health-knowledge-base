/**
 * 根据情绪标签获取匹配的知识片段
 */
export function getKnowledgeSnippets(emotions: string[], count: number = 2): KnowledgeSnippet[] {
  // 收集所有匹配的片段
  const matched: KnowledgeSnippet[] = [];
  const general: KnowledgeSnippet[] = [];

  for (const snippet of snippets) {
    if (snippet.emotions.length === 0) {
      general.push(snippet);
    } else if (snippet.emotions.some(e => emotions.includes(e))) {
      matched.push(snippet);
    }
  }

  // 优先返回情绪匹配的片段，补充通用片段
  const result: KnowledgeSnippet[] = [];
  const used = new Set<string>();

  // 先取匹配的
  const shuffled = [...matched].sort(() => Math.random() - 0.5);
  for (const s of shuffled) {
    if (!used.has(s.text) && result.length < count) {
      result.push(s);
      used.add(s.text);
    }
  }

  // 再取通用的
  if (result.length < count) {
    const shuffledGeneral = [...general].sort(() => Math.random() - 0.5);
    for (const s of shuffledGeneral) {
      if (!used.has(s.text) && result.length < count) {
        result.push(s);
        used.add(s.text);
      }
    }
  }

  return result;
}

/**
 * 获取单个知识片段（用于在回复中自然插入）
 */
export function getRandomKnowledgeSnippet(emotions: string[]): KnowledgeSnippet | null {
  const snippets = getKnowledgeSnippets(emotions, 1);
  return snippets.length > 0 ? snippets[0] : null;
}